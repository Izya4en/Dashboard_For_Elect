-- 1. СОЗДАНИЕ ТАБЛИЦ
-- ==============================================================================

CREATE TABLE IF NOT EXISTS organizations (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL,
    bin VARCHAR(12) UNIQUE,
    lat DOUBLE PRECISION DEFAULT 51.169392,
    lon DOUBLE PRECISION DEFAULT 71.449074
);

CREATE TABLE IF NOT EXISTS service_types (
    id SERIAL PRIMARY KEY,
    name VARCHAR(255) NOT NULL
);

-- Удаляем уже существующие полные дубликаты сервисов перед применением уникального ограничения
DELETE FROM service_types a
USING service_types b
WHERE a.id > b.id AND a.name = b.name;

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint c
        JOIN pg_class t ON t.oid = c.conrelid
        WHERE c.contype = 'u'
          AND t.relname = 'service_types'
          AND c.conname = 'service_types_name_key'
    ) THEN
        ALTER TABLE service_types ADD CONSTRAINT service_types_name_key UNIQUE (name);
    END IF;
END$$;

CREATE TABLE IF NOT EXISTS acts (
    id SERIAL PRIMARY KEY,
    test_date DATE NOT NULL,
    organization_id INT REFERENCES organizations(id) ON DELETE CASCADE,
    service_type_id INT REFERENCES service_types(id) ON DELETE CASCADE
);

